#include"nixie.h"
#include"delay.h"
#include"ch451.h"

#include<reg51.h>

unsigned char num[]={0x3f,0x06,0x5b,0x4f,0x66,0x6d,0x7d,0x07,0x7f,0x6f,0x77,0x7c,0x58,0x5e,0x79,0x71,0x00};

unsigned short pos[]={0x0f00,0x0e00,0x0d00,0x0c00,0x0b00,0x0a00,0x0900,0x0800};

void NixieDisplay(unsigned char d[]){
	int i;
	for(i=0;i<8;i++){
		if(i==6) ch451_write(pos[i]|num[d[i]]|0x80);
		else ch451_write(pos[i]|num[d[i]]);
	}
}